import {
  AchievementRewardModel,
  AchievementRarity,
} from '../models/achievement.model';

export type AchievementMetric =
  | 'quizPlayed'
  | 'correctAnswers'
  | 'level'
  | 'xp'
  | 'streakDays'
  | 'accuracy'
  | 'avatarsUnlocked'
  | 'tutorialCompleted';

export interface AchievementDefinition {
  id: string;
  icon: string;
  title: string;
  description: string;
  metric: AchievementMetric;
  target: number;
  minAnswers?: number;
  reward: AchievementRewardModel;
  rarity: AchievementRarity;
}

const createTitleReward = (
  titleId: string,
  titleIcon: string,
  titleLabel: string,
  titleRarity: AchievementRarity,
): AchievementRewardModel => ({
  type: 'title',
  label: `Titolo: ${titleLabel}`,
  titleId,
  titleIcon,
  titleLabel,
  titleRarity,
});

